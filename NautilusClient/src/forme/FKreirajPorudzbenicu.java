    private void jbtnsacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnsacuvajActionPerformed
        // TODO add your handling code here:
        ModelTabeleStavki mts = (ModelTabeleStavki) jtblstavke.getModel();
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        try {
            List<StavkaPorudzbenice> listaS = mts.getListaStavki();
            if (listaS.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Porudzbenica mora imati barem 1 stavku");
                return;
            }
            Date datum = sdf.parse(jlabdatum.getText());
            double ukupnopdv = Double.parseDouble(jtxtukupnasapdvom.getText());
            double ukupnobezpdv = Double.parseDouble(jtxtukupnabezpdva.getText());
            if (jradiolista.isSelected() && jradiografik.isSelected()) {
                JOptionPane.showMessageDialog(fp, "Obelezili ste i listu i grafik! ");
                return;
            }
            if (!jradiolista.isSelected() && !jradiografik.isSelected()) {
                JOptionPane.showMessageDialog(fp, "Morate obeleziti listu ili grafik! ");
                return;
            }
            Sto sto = new Sto();
            if (jradiolista.isSelected()) {
                sto = (Sto) jcombosto.getSelectedItem();
            } else {
                if (dodatjegrafik == true) {
                    sto = stoGrafik;
                } else {
                    JOptionPane.showMessageDialog(this, "Niste obelezili sto na grafiku!");
                }
            }
//            Sto sto = (Sto) jcombosto.getSelectedItem();
            if (sto.isStatus() == 1) {
                JOptionPane.showMessageDialog(this, "Izabrani sto je zauzet");
                return;
            }

            byte stat = 1;
            sto.setStatus(stat);
            ServerskiOdgovor so1 = Kontroler.getInstanca().izmeniSto(sto);

            if (so1.isUspesno()) {
                JOptionPane.showMessageDialog(this, "Zauzeli ste sto! ");
            } else {
                JOptionPane.showMessageDialog(this, "Ne mozete zauzeti ovaj sto");
            }
            Porudzbenica p = new Porudzbenica(-1, datum, ukupnopdv, ukupnobezpdv, (Sto) sto, (Korisnik) korisnik, listaS);

            ServerskiOdgovor so = Kontroler.getInstanca().sacuvajPorudzbenicu(p);

            if (so.isUspesno()) {
                ocistiFormu();
                JOptionPane.showMessageDialog(this, so.getPoruka());
            } else {
                JOptionPane.showMessageDialog(this, so.getPoruka());
            }
        } catch (ParseException ex) {
            Logger.getLogger(FKreirajPorudzbenicu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbtnsacuvajActionPerformed

