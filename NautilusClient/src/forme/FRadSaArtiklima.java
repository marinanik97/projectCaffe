    public void sreditabelu() {
        Artikal a = new Artikal();
        a.setNazivArtikla("");

        ServerskiOdgovor so = Kontroler.getInstanca().pronadjiArtikle(a);

        if (so.isUspesno()) {
            List<InterfaceObjekat> listaArtikla = (List<InterfaceObjekat>) so.getOdgovor();
            ModelTabeleArtikli mta = new ModelTabeleArtikli();
            mta.setLista(listaArtikla);
            jTable2.setModel(mta);
        } else {
            JOptionPane.showMessageDialog(this, "Neuspesno ucitana lista artikala", "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }

    void refresh() {
        ModelTabeleArtikli mtp = (ModelTabeleArtikli) jTable2.getModel();
        mtp.refresh();
    }

    public void srediTabelu2() {
        Artikal a = new Artikal();
        a.setNazivArtikla(jTextField1.getText());

        ServerskiOdgovor so = Kontroler.getInstanca().pronadjiArtikle(a);

        if (so.isUspesno()) {
            List<InterfaceObjekat> listaartikala = (List<InterfaceObjekat>) so.getOdgovor();
            ModelTabeleArtikli mta = (ModelTabeleArtikli) jTable2.getModel();
            mta.setLista(listaartikala);
            if (listaartikala.isEmpty()) {
                tab = 0;

            } else {
                tab = 1;

            }
        } else {
            tab = 2;

        }
    }

    private void poruka(int tab) {
        switch (tab) {
            case 0:
                JOptionPane.showMessageDialog(this, "Sistem ne moze da pronadje artikle po zadatim kriterijumima pretrage");
                break;
            case 1:
                JOptionPane.showMessageDialog(this, "Sistem je pronasao artikle po zadatim kriterijumima pretrage");
                break;
            case 2:
                JOptionPane.showMessageDialog(this, "Sistem ne moze da pronadje artikle po zadatim kriterijumima pretrage");
                break;
        }
    }

