    private void jbtnobrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnobrisiActionPerformed
        // TODO add your handling code here:
        int red = jTable2.getSelectedRow();
        if (red != -1) {
            ModelTabeleArtikli mta = (ModelTabeleArtikli) jTable2.getModel();
            Artikal a = mta.vratiSelektovaniArtikal(red);
            int response = JOptionPane.showConfirmDialog(this, "Da li sigurno zelite da obrisete artikal " + a + "?", "Brisanje artikla", JOptionPane.YES_NO_OPTION);
            if (response == JOptionPane.OK_OPTION) {

                ServerskiOdgovor so = Kontroler.getInstanca().obrisiArtikal(a);

                if (so.isUspesno()) {
                    JOptionPane.showMessageDialog(this, so.getPoruka(), "Uspesno", JOptionPane.INFORMATION_MESSAGE);
                    sreditabelu();

                } else {
                    JOptionPane.showMessageDialog(this, so.getPoruka(), "Neuspesno", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Sistem nije obrisao artikal", "Greska", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_jbtnobrisiActionPerformed

